// Operators
// Adição +
let adicao = 2 + 1;

// Subtração -
let subtracao = 2 - 1;

// Multiplicação *
let multiplicacao = 2 * 1;

// Divisão /
let divisao = 2 / 1;

// Módulo %
let modulo = 2 % 1;

// Exponenciação **
let exponenciacao = 2 ** 2;

// Incremento ++
let incremento = 2;
incremento++;

// Descremento --
let decremento = 2;
decremento--;

console.log(adicao, subtracao, multiplicacao, divisao, modulo, exponenciacao, incremento, decremento);