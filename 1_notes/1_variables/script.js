// Variables
// Tipo da variavel + nome da variavel = valor da variavel;

// Let
// Redeclaração: Apenas em escopos diferentes;
// Reatribuíção: Sim;
// Escopo (espaço no qual as variáveis podem ser acessadas): Bloco;
// Hoisting: Não.
let nome = 'Marcos';


// Const
// Redeclaração: Não;
// Reatribuíção: Não;
// Escopo (espaço no qual as variáveis podem ser acessadas): Bloco;
// Hoisting: Não.
const sobreNome = 'Barros';


// Var - Pode ser redeclarada e reatribuída
// Redeclaração: Sim;
// Reatribuíção: Sim;
// Escopo (espaço no qual as variáveis podem ser acessadas): Função;
// Hoisting: Sim.
var idade = 30;
