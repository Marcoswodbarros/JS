// Comparison operators

// Variables eg.
let nome = 'Marcos';
let age = 30;


// Igual em valor ==
nome == 'Marcos'; // True

// Igual em valor e tipo ===
age === 30; // True

// Diferente em valor !=
nome != 'Tiago'; // True

// Diferente em valor e tipo !==
age !== '30'; // True

// Maior que >
age > 29; // True

// Menor que <
age < 31; // True

// Maior ou igual a >=
age >= 30; // True

// Menor ou igual a <=
age <= 30; // True
